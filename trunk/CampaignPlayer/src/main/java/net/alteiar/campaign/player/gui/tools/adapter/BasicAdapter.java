package net.alteiar.campaign.player.gui.tools.adapter;

import java.util.Observable;

/**
 * @author Cody Stoutenburg
 * 
 */
public abstract class BasicAdapter<E> extends Observable {
	private final E src;

	public BasicAdapter(E src) {
		this.src = src;
	}

	public E getObject() {
		return src;
	}

	public void endModify() {
		this.setChanged();
		this.notifyObservers();
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((src == null) ? 0 : src.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		BasicAdapter<?> other = (BasicAdapter<?>) obj;
		if (src == null) {
			if (other.src != null)
				return false;
		} else if (!src.equals(other.src))
			return false;
		return true;
	}

	@Override
	public abstract String toString();
}
